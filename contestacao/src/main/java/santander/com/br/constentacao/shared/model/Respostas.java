public class Respostas {

    String descricao;
    String dica;
    String proxima_questao;

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getDica() {
        return dica;
    }

    public void setDica(String dica) {
        this.dica = dica;
    }

    public String getProxima_questao() {
        return proxima_questao;
    }

    public void setProxima_questao(String proxima_questao) {
        this.proxima_questao = proxima_questao;
    }
}
